#include<bits/stdc++.h>
using namespace std;
int main()
{
    int a;
    cin >> a;
    switch (a)
    {
    case 1:
        cout << "a is 1" << endl;
        break;
    case 2:
        cout << "a is 2" << endl;
        break;
    case 3:
        cout << "a is 3" << endl;
        break;
    default:
        cout << "a is not 1, 2 or 3" << endl;
        break;
    }
    return 0;
}